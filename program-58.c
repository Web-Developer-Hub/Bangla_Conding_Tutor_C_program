//increment or decrement in c program....
# include <stdio.h>
int main() {
    int x = 5;
    printf("Value of x is: %d \n", x++);
    printf("Value of x is: %d \n", ++x);

    x = 5;
    printf("Value of x is: %d \n", ++x);
    printf("Value of x is: %d \n", x++);

    printf("\n");

    x = 5;
    printf("Value of x is: %d \n", x--);
    printf("Value of x is: %d \n", --x);

    x = 5;
    printf("Value of x is: %d \n", --x);
    printf("Value of x is: %d \n", x--);

return 0;
}

