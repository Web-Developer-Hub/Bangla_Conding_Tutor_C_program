#include <stdio.h>
int main() {
    char x = 'A', y = 'v', z = 'n', n = 'M';
    printf("%c -> %d\n", x, x);
    printf("%c -> %d\n", y, y);
    printf("%c -> %d\n", z, z);
    printf("%c -> %d\n", n, n);

    int a = 34, b = 55, c = 100, d = 63, e = 117;
    printf("%d -> %c \n", a, a);
    printf("%d -> %c \n", b, b);
    printf("%d -> %c \n", c, c);
    printf("%d -> %c \n", d, d);
    printf("%d -> %c \n", e, e);

return 0;
}
