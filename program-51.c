// Nested if else in c program..

#include <stdio.h>
int main() {
    char ch = 'h';

    if(ch == 'a') {
        printf("Apple. \n");
    }
    else if(ch == 'b'){
        printf("Banana. \n");
    }
    else if(ch == 'm') {
        printf("Mango. \n");
    }
    else if(ch == 'n'){
        printf("North. \n");
    }
    else if(ch == 'h'){
        printf("Hello habib. \n");
    }
    else if(ch == 'c'){
        printf("c program. \n");
    }
    else{
        printf("don\'t match all though condition. \n");
    }

return 0;
}
